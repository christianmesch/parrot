function parrot() {
    let text = document.getElementById('text').value;
    let primary = document.getElementById('primary').value;
    let secondary = document.getElementById('secondary').value;

    var result = '';
    for (var row = 0; row < 5; row++) {
        var tmp = '';
        for (let c of text) {
            tmp += alphabet[c.toLowerCase()][row]
                .replace(new RegExp('#', 'g'), primary)
                .replace(new RegExp('¤', 'g'), secondary);
        }
        
        result += tmp + '\n';
    }

    document.getElementById('result').value = result.trim();
}

